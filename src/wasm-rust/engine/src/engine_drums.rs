// ============ Category lookup ============

fn drum_category(midi_note: i16) -> u8 {
    match midi_note {
        35 | 36 => 0,                         // Kick
        37 | 38 | 39 | 40 => 1,               // Snare
        42 | 44 | 46 => 2,                     // HiHat
        41 | 43 | 45 | 47 | 48 | 50 => 3,     // Tom
        49 | 51 | 52 | 53 | 55 | 57 | 59 => 4, // Cymbal
        _ => 5,                                // Perc
    }
}

static CATEGORY_POOLS: [&[DrumPattern]; 6] = [
    KICK, SNARE, HIHAT, TOM, CYMBAL, PERC,
];

/// Pick a pattern for the given drum MIDI note using the random index.
pub fn get_drum_pattern(midi_note: i16, rng_val: u32) -> &'static DrumPattern {
    let cat = drum_category(midi_note) as usize;
    let pool = CATEGORY_POOLS[cat];
    &pool[(rng_val as usize) % pool.len()]
}
