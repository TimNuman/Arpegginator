// engine_drums.rs — Curated drum pattern library for random note creation

pub struct DrumPattern {
    pub amount: u8,      // repeat_amount (steps in pattern)
    pub space_mul: u8,   // repeat_space = space_mul * tpc
    pub vel: [u8; 16],   // velocity per step (0-100)
    pub hit: [u8; 16],   // hit probability per step (0/60/100)
}

// Helper: concise pattern construction
const fn dp(amount: u8, space: u8, vel: [u8; 16], hit: [u8; 16]) -> DrumPattern {
    DrumPattern { amount, space_mul: space, vel, hit }
}

